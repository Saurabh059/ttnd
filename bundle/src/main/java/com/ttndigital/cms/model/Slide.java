package com.ttndigital.cms.model;

public class Slide {
	private String sliderImage;
	private String sliderHeader;
	private String sliderFooter;
	private String sliderContentImage;

	public String getSliderImage() {
		return sliderImage;
	}

	public void setSliderImage(String sliderImage) {
		this.sliderImage = sliderImage;
	}

	public String getSliderHeader() {
		return sliderHeader;
	}

	public void setSliderHeader(String sliderHeader) {
		this.sliderHeader = sliderHeader;
	}

	public String getSliderFooter() {
		return sliderFooter;
	}

	public void setSliderFooter(String sliderFooter) {
		this.sliderFooter = sliderFooter;
	}

	public String getSliderContentImage() {
		return sliderContentImage;
	}

	public void setSliderContentImage(String sliderContentImage) {
		this.sliderContentImage = sliderContentImage;
	}

}
