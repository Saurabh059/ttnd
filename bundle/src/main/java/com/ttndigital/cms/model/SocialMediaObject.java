package com.ttndigital.cms.model;

/**
 * Model for Social Media Connect Name and Link.
 */
public class SocialMediaObject {

    private String name;

    private String link;

    public String getLink() {
        return link;
    }

    public String getName() {
        return name;
    }

    public void setLink(String link) {
        this.link = link;
    }

    public void setName(String name) {
        this.name = name;
    }


}
